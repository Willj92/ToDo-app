module.exports.somar = function(num1, num2) {
    return num1 + num2;
};

module.exports.subtrair = function(num1, num2) {
    return num1 - num2;
};

module.exports.multiplicar = function(num1, num2) {
    return num1 * num2;
};

module.exports.dividir = function(num1, num2) {
    return num1 / num2;
};
